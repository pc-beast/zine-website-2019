<div class="blogPageSubHeading">Arithmetic-Logic Unit</div><br>

It is the part of a microprocessor that carries out the arithmetic and logical operations inside a microprocessor. The inputs to an ALU are <br><br>
<ol>
<li> data to be operated on, called operands, and </li>
<li>a code indicating the operation to be performed;</li><br><br>
</ol>
The output of the ALU is the result of the performed operation. For example, you gave your microprocessor a task to calculate 1+2, then 1 and 2 are the operands, and the ‘+’ is the instruction that tells what to do with the operands, this was an example of arithmetic operation. What are logical operations then? AND, OR, and NOT are all logical operations. Were you able to guess them? <br><br>

<div class="blogPageSubHeading">Control Unit</div><br>

Now that our ALU has given a task to calculate 1+2, ALU deduces that we have to add 1 and 2 but control is needed for the instruction so provided to add numbers to ALU. Hence, the control unit came into the picture. The control unit directs the operation of the microprocessor, it tells the memory, ALU, input, and output devices about how to respond to the instructions given to them.<br><br>
<br><br>
<div class="blogPageSubHeading">Register array</div><br>

Our processor now knows what to do and how to do it, but we need to have some space to store that data. So, microprocessors have register arrays for that purpose. <br><br>
But what is a register? <br><br>

A register is a storage element, quickly accessible location available to microprocessors.<br><br>
Arrange multiple registers to get an array of registers to store more data.<br><br>
